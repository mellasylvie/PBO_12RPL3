def luas_persegi(sisi):
    luas = sisi * sisi
    return luas

print(luas_persegi(5))

def luas_segitiga(alas, tinggi):
    luas = alas * tinggi /2
    return luas

print(luas_segitiga(8, 7))

def luas_lingkaran(r):
    luas = 3.14 * r * r
    return luas

print(luas_lingkaran(6))






